<section class="faq">
    <h2>Questions fréquentes</h2>

    <h3>Tout savoir sur le Techflow Points</h3>
    <p>Le Techflow Points est encore mieux qu’un programme de fidélité. Finie la carte de fidélité, tout est dématérialisé.</p>

    <h3>Le choix de la simplicité</h3>
    <p>Avec l’offre simplicité, 1% est cagnotté à chaque achat et vous bénéficiez d’offres exclusives toute l’année.</p>

    <h3>Le choix de la facilité</h3>
    <p>Avec l’offre facilité, vous cumulez 2% à chaque achat, profitez d’offres exclusives toute l’année, de la livraison offerte sans minimum d’achat, d’un chèque-cadeau de 6€ pour votre anniversaire et d’un accès prioritaire au service client.</p>

    <h3>Le choix de la sérénité</h3>
    <p>Avec l’offre sérénité, vos réparations sont en illimité, vous cumulez 2% à chaque achat, bénéficiez d’offres exclusives toute l’année, de la livraison offerte sans minimum d’achat, d’un chèque-cadeau de 6€ pour votre anniversaire et d’un accès prioritaire au service client.</p>

    <h3>Tout savoir sur le rachat de produit</h3>
    <p>Estimez votre appareil en quelques minutes sur Techflow.com ou en magasin avec un conseiller. Un montant vous est proposé sous forme de carte-cadeau ou de remise immédiate si le rachat est couplé à l’achat d’un nouveau produit.</p>
    <p>Votre ancien produit est ensuite réintroduit dans un circuit vertueux. Selon son état, il est reconditionné pour une seconde vie ou recyclé pour pièces détachées.</p>

    <h3>Revendre mon ancien appareil</h3>
    <p>Vous déduisez l’estimation de votre ancien appareil lors de l’achat d’un nouveau produit. Sur Techflow.com et en magasin, plusieurs types d’appareils peuvent être revendus : téléphone portable, tablette tactile, MacBook et PC portable, consoles de jeux.</p>

    <h3>Aide à la réparation : techREPAR</h3>
    <p>Grâce à la labellisation QualiRépar, en faisant appel aux services Techflow pour un produit hors garantie, vous bénéficiez automatiquement d’une remise sur le montant des réparations, sans démarche supplémentaire.</p>

    <h3>Qu’est-ce que le label QualiRépar ?</h3>
    <p>Dans le cadre de la loi AGEC, un fonds dédié au financement de la réparation a été créé pour encourager les consommateurs à prolonger la durée d’usage de leurs équipements hors garantie.</p>
    <p>Les réparateurs labellisés techREPAR peuvent faire bénéficier leurs clients d’une remise sur la réparation de leurs appareils hors garantie, pouvant aller jusqu’à environ 20% du montant total de la réparation.</p>
